// MOVELIST SHUFFLE
movieList.sort( function() { return 0.5 - Math.random() } );


//---------------------MOVIE FILTER-------------------------

$(document).ready(function() {
    $('.filter li a').click(function() {
        // get class of the clicked item
        var clickedClass = $(this).attr('class');
        // reset active class on all options
        $('.filter li').removeClass('active');
        // update the active state on clicked button
        $(this).parent().addClass('active');
    
        if(clickedClass == 'all') {
            $('.items').children('div.item').show();    
        }
        else {
        // hide all elements 
        $('.items').children('div:not(.' + clickedClass + ')').hide();
        // show all elements that have this Class
        $('.items').children('div.' + clickedClass).show();
        }
        return false;
    });
});


