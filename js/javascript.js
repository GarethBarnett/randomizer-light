//---------------------DIV CREATION-------------------------

// Where the div will be referenced to
var target = document.querySelector('.items');

//loop to create how many divs are in the object array
for(var i = 0; i< 16; i++){
    
    // Create the new div
    var div = document.createElement('div');

    // div.className = ('item');
    // Add content to the new element
    div.innerHTML = '<img src="' + movieList[i].image+'" alt="'+ (i+1) +'"/>';
    // Apply classes
    div.className = movieList[i].classNames;

    // // Insert the element before our target element
    target.insertBefore(div, target.childNodes[0]); 
}




