var title = document.querySelector('.title');
var info = document.querySelectorAll('.info');
var description = document.querySelector('.description');
var image = document.querySelector('.image');
var i = 0;


// when run, check if we are at START of movie list and update content

function findMovie() {
    if ( i === 0 ) {
        i=movieList.length - 1;
        changeMovie();
    } 
}


//On run update contents in modal
function changeMovie() {

    image.src = movieList[i].image;
    title.innerText = movieList[i].title;
    info.innerText = movieList[i].info;
    description.innerText = movieList[i].description;
    
    $('.image').replaceWith('<img class="image" src="' + image.src + '" />');
    $('.title').replaceWith('<h1 class="title">' + title.innerText + '</h1>');
    $('.info').replaceWith('<h4 class="model">' + info.innerText + '</h4>');
    $('.description').replaceWith('<p class="description">' + description.innerText + '</p>');
}

